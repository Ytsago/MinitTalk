#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s && s[i])
		i++;
	return (i);
}

size_t	ft_tablen(char **s)
{
	size_t	i;

	i = 0;
	while (s && s[i])
		i++;
	return (i);
}
